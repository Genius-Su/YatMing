using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using iTelluro.Explorer.YatMing.Application.DTO;
using iTelluro.Explorer.YatMing.Domain.Entities;

namespace iTelluro.Explorer.YatMing.IApplication
{
    /// <summary>
    /// TEmployee基础方法接口
    /// </summary>
    public interface ITEmployeeApp
    {
        /// <summary>
        /// 新增一条TEmployee记录
        /// </summary>
        /// <param name="dto">TEmployee实体</param>
        /// <returns>是否新增成功</returns>
        bool Add(TEmployeeDTO dto);

        /// <summary>
        /// 根据主键删除一条TEmployee记录
        /// </summary>
        /// <param name="guid">TEmployee主键值</param>
        /// <returns>是否删除成功</returns>
        bool Remove(string guid);

        /// <summary>
        /// 更新一条TEmployee记录
        /// </summary>
        /// <param name="dto">TEmployee实体</param>
        /// <returns>是否更新成功</returns>
        bool Update(TEmployeeDTO dto);

        /// <summary>
        /// 根据TEmployee主键获取一条记录
        /// </summary>
        /// <param name="guid">TEmployee主键值</param>
        /// <returns>根据主键查询到的TEmployee记录</returns>
        TEmployeeDTO Get(string guid);

        /// <summary>
        /// 查询所有TEmployee记录
        /// </summary>
        /// <returns>TEmployee所有记录集合</returns>
        List<TEmployeeDTO> GetAll();

    }
}
